// camelCase： 小驼峰
// kebabCase： 转换为-连接，例如：Foo Bar ----> foo-bar
// snakeCase： 转化为蛇形风格
// capitalize： 首字母大写
// lowerFirst:  首字母小写
// upperFirst：首字母转为大写
// endsWith： 判断结尾字符
// startsWith: 判断开头字符
// escape： 字符转义处理, 与unescape相反
// lowerCase: 空格分开单词，并转换为小写
// upperCase： 空格分隔单词，并转换为大写
// pad： 字符填充为指定长度
// repeat： 字符串重复
// replace： 字符替换
// split: 切割拆分
// toLower： 全部转换为小写
// toUpper:  全部转换为大写
// trim: 移除前面和后面的 空格 或 指定的字符
// words: 拆分词为数组
